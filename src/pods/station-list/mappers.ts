import * as APIModel from './api';
import { provinces as provinceNames } from './consts';
import * as StoreModel from './store/model';

export const mapProvincesAPItoStore = (
  apiProvinces: APIModel.Province[]
): StoreModel.Province[] => {
  let provincesReturn: StoreModel.Province[] = [];
  apiProvinces.map((apiProvince: APIModel.Province) => {
    provincesReturn.push({
      id: parseInt(apiProvince.provincia),
      name: provinceNames[apiProvince.provincia]
    });
  });
  return provincesReturn;
};


export const mapMunicipalitiesAPItoStore = (apiMunicipalities: APIModel.Municipality[]): StoreModel.Municipality[] => {
  let municipalitiesReturn: StoreModel.Municipality[] = [];
  let autoGeneratedId = 0;
  apiMunicipalities.map((apiMunicipality: APIModel.Municipality) => {
    municipalitiesReturn.push({ id: autoGeneratedId, name: apiMunicipality.municipi })
    autoGeneratedId++;
  })
  return municipalitiesReturn;
}

export const mapProvinceStoreToAPI = (province: StoreModel.Province): APIModel.Province => ({ provincia: String(province.id) })